document.addEventListener('DOMContentLoaded', () => {
    
    // --- 1. PRELOADER ---
    const preloader = document.getElementById('preloader');
    if (preloader) {
        setTimeout(() => {
            preloader.style.opacity = '0';
            setTimeout(() => { preloader.style.display = 'none'; }, 700);
        }, 500);
    }

    // --- 2. STICKY NAVBAR ---
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) navbar.classList.add('navbar-scrolled');
        else navbar.classList.remove('navbar-scrolled');
    });

    // --- 3. MOBILE MENU ---
    const mobileBtn = document.getElementById('mobile-menu-btn');
    const mobileMenuCard = document.getElementById('mobile-menu-card');
    const mobileLinks = document.querySelectorAll('.mobile-link');
    const burgerLines = document.querySelectorAll('.burger-line');
    let isMenuOpen = false;

    function toggleMenu() {
        isMenuOpen = !isMenuOpen;
        if (isMenuOpen) {
            mobileMenuCard.classList.remove('opacity-0', 'pointer-events-none', 'translate-y-[-10px]');
            mobileMenuCard.classList.add('opacity-100', 'pointer-events-auto', 'translate-y-0');
            burgerLines[0].style.transform = 'translateY(8px) rotate(45deg)';
            burgerLines[1].style.opacity = '0';
            burgerLines[2].style.transform = 'translateY(-8px) rotate(-45deg)';
            document.body.style.overflow = 'hidden';
        } else {
            mobileMenuCard.classList.add('opacity-0', 'pointer-events-none', 'translate-y-[-10px]');
            mobileMenuCard.classList.remove('opacity-100', 'pointer-events-auto', 'translate-y-0');
            burgerLines[0].style.transform = 'translateY(0) rotate(0)';
            burgerLines[1].style.opacity = '1';
            burgerLines[2].style.transform = 'translateY(0) rotate(0)';
            document.body.style.overflow = '';
        }
    }

    if (mobileBtn && mobileMenuCard) {
        mobileBtn.addEventListener('click', toggleMenu);
        mobileLinks.forEach(link => link.addEventListener('click', () => { if(isMenuOpen) toggleMenu(); }));
        document.addEventListener('click', (e) => {
            if (isMenuOpen && !mobileMenuCard.contains(e.target) && !mobileBtn.contains(e.target)) toggleMenu();
        });
    }

    // --- 4. SCROLL REVEAL ANIMATION ---
    const revealElements = document.querySelectorAll('.reveal');
    const revealObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.15, rootMargin: "0px 0px -50px 0px" });
    revealElements.forEach(el => revealObserver.observe(el));

    // --- 5. SMOOTH SCROLLING ---
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                e.preventDefault();
                targetElement.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });

    // --- 6. SPOTLIGHT EFFECT ---
    const spotlightCards = document.querySelectorAll('.spotlight-card');
    spotlightCards.forEach(card => {
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            card.style.setProperty('--x', `${x}px`);
            card.style.setProperty('--y', `${y}px`);
        });
    });

    // --- 7. PRICING TOGGLE ---
    const pricingToggle = document.getElementById('billing-toggle');
    const priceElements = document.querySelectorAll('.price-val');
    if (pricingToggle) {
        pricingToggle.addEventListener('change', (e) => {
            const isAnnual = e.target.checked;
            priceElements.forEach(el => {
                const monthly = el.getAttribute('data-monthly');
                const annual = el.getAttribute('data-annual');
                if(monthly && annual) {
                    el.textContent = isAnnual ? annual : monthly;
                }
            });
        });
    }

    // --- 8. FAQ ACCORDION ---
    const faqItems = document.querySelectorAll('.faq-item');
    faqItems.forEach(item => {
        const button = item.querySelector('button');
        const content = item.querySelector('.faq-content');
        button.addEventListener('click', () => {
            const isActive = item.classList.contains('active');
            // Close all others
            faqItems.forEach(otherItem => {
                otherItem.classList.remove('active');
                otherItem.querySelector('.faq-content').style.maxHeight = null;
            });
            // Open clicked if it wasn't active
            if (!isActive) {
                item.classList.add('active');
                content.style.maxHeight = content.scrollHeight + "px";
            }
        });
    });

    // --- 9. LANGUAGE TOGGLE ---
    const langBtns = document.querySelectorAll('.lang-toggle-btn');
    let currentLang = 'en';

    function updateLanguage(lang) {
        currentLang = lang;
        const dict = translations[lang];
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (dict[key]) {
                el.textContent = dict[key];
            }
        });
        document.documentElement.lang = lang;
        
        // Update active state of buttons
        langBtns.forEach(btn => {
            if(btn.getAttribute('data-lang') === lang) {
                btn.classList.add('bg-white', 'text-dark-900');
                btn.classList.remove('text-gray-400', 'hover:text-white');
            } else {
                btn.classList.remove('bg-white', 'text-dark-900');
                btn.classList.add('text-gray-400', 'hover:text-white');
            }
        });
    }

    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            updateLanguage(btn.getAttribute('data-lang'));
        });
    });

    // --- 10. ROI CALCULATOR LOGIC ---
    const roiSlider = document.getElementById('roi-slider');
    const roiEmpCount = document.getElementById('roi-emp-count');
    const roiHours = document.getElementById('roi-hours');
    const roiMoney = document.getElementById('roi-money');

    if(roiSlider && roiEmpCount && roiHours && roiMoney) {
        roiSlider.addEventListener('input', (e) => {
            const employees = parseInt(e.target.value);
            roiEmpCount.textContent = employees;
            
            // Assumption: each employee spends 10h/week on manual tasks. 52 weeks = 520h/year.
            // Assumption: average hourly cost = $15.
            const hoursSaved = employees * 520;
            const moneySaved = hoursSaved * 15;
            
            // Animate numbers (simple instantaneous update here, could use a counter library)
            roiHours.textContent = hoursSaved.toLocaleString();
            roiMoney.textContent = "$" + moneySaved.toLocaleString();
        });
    }

    // --- 11. BEFORE / AFTER SLIDER ---
    const baContainer = document.querySelector('.ba-container');
    const baAfter = document.querySelector('.ba-after');
    const baHandle = document.querySelector('.ba-handle');
    
    if (baContainer && baAfter && baHandle) {
        let isDragging = false;
        
        const updateSlider = (x) => {
            const rect = baContainer.getBoundingClientRect();
            let position = ((x - rect.left) / rect.width) * 100;
            position = Math.max(0, Math.min(position, 100));
            baAfter.style.width = `${position}%`;
            baHandle.style.left = `${position}%`;
        };

        baContainer.addEventListener('mousedown', (e) => { isDragging = true; updateSlider(e.clientX); });
        window.addEventListener('mousemove', (e) => { if (isDragging) updateSlider(e.clientX); });
        window.addEventListener('mouseup', () => { isDragging = false; });
        
        // Touch support
        baContainer.addEventListener('touchstart', (e) => { isDragging = true; updateSlider(e.touches[0].clientX); });
        window.addEventListener('touchmove', (e) => { if (isDragging) updateSlider(e.touches[0].clientX); });
        window.addEventListener('touchend', () => { isDragging = false; });
    }

});
